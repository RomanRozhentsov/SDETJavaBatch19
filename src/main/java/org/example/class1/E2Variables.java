public class E2Variables {

    public static void main(String[] args) {
    //Data type for storing decimal numbers (10.2):
    //"Float" is data type for storing the smallest decimal numbers; f - at the end of number means - float.
        float price=12.3f;
        System.out.println(price);
    //"Double data type for storing bigger decimal number; for "double" i don't need anything at the end.
        double number=12.2323232232;
        System.out.println(number);
    //Variables are used to store information.
    //Data type - tells the computer what type of data we're planning to store.

    }



}
