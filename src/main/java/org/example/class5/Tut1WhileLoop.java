package org.example.class5;

public class Tut1WhileLoop {
    public static void main(String[] args) {

        int a = 0;

        for(int i =0; i<10; i++){
            System.out.println("i is less than 10 "+i);
        }

        while(a<10){
            System.out.println("a is less than 10 "+a);
            a++;
        }
    }
}
