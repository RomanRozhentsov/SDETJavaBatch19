package org.example.class8;

public class E5_2DArrays {
    public static void main(String[] args) {
        // Outer Loop + Inner Loop;

        for(int j = 0; j<7; j++){
            for(int i = 0; i<4; i++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
